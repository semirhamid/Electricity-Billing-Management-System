    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        String rid = recordidfield.getText();
        String cid = customeridfield.getText();
        String mcount = metercountfield.getText();
        String description = descriptionfield.getText();
        String status = statusfield.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();

            st.executeUpdate("update record set rid ='"+rid+"',cid = '"+cid+"',mcount = '"+mcount+"',description='"+description+"',status='"+status+"' where rid='"+rid+"'");
        JOptionPane.showMessageDialog(null,"successfully updated");
        setVisible(false);
       new UpdateRecord().setVisible(true);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }//GEN-LAST:event_updatebuttonActionPerformed

