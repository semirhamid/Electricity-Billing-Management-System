    private void searchbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbuttonActionPerformed
        String rid = recordidfield.getText();
        try{
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from record where rid='"+rid+"'");
            if(rs.next()){
                customeridfield.setText(rs.getString(2));
                metercountfield.setText(rs.getString(3));
                descriptionfield.setText(rs.getString(4));
                statusfield.setText(rs.getString(5));
                datefield.setText(rs.getString(6));
                
                recordidfield.setEditable(false);
                datefield.setEditable(false);
                
            }else{
                JOptionPane.showMessageDialog(null,"Record id doesnot exist");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }//GEN-LAST:event_searchbuttonActionPerformed

